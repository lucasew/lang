func hasPOSSub(atr *languagetool.AnalyzedTokenReadings, sub string) bool {
	if atr == nil {
		return false
	}
	for _, r := range atr.GetReadings() {
		if r != nil && r.GetPOSTag() != nil && strings.Contains(*r.GetPOSTag(), sub) {
			return true
		}
	}
	return false
}

func readingsDump(atr *languagetool.AnalyzedTokenReadings) string {
	if atr == nil {
		return ""
	}
	var b []string
	for _, r := range atr.GetReadings() {
		if r != nil && r.GetPOSTag() != nil {
			b = append(b, *r.GetPOSTag())
		}
	}
	return strings.Join(b, ",")
}

func atrSent2(a, b string) *languagetool.AnalyzedSentence {
	return mwSent(a, b)
}

// mwSent builds SENT_START + content tokens (no whitespace between) for MultiWordChunker2.
func mwSent(parts ...string) *languagetool.AnalyzedSentence {
	st := languagetool.SentenceStartTagName
	toks := []*languagetool.AnalyzedTokenReadings{
		languagetool.NewAnalyzedTokenReadings(languagetool.NewAnalyzedToken("", &st, nil)),
	}
	for _, p := range parts {
		pos := "x"
		lem := p
		toks = append(toks, languagetool.NewAnalyzedTokenReadings(
			languagetool.NewAnalyzedToken(p, &pos, &lem)))
	}
	return languagetool.NewAnalyzedSentence(toks)
}

func contentPOSTagsMW(out *languagetool.AnalyzedSentence) [][]string {
	var all [][]string
	for i, tr := range out.GetTokens() {
		if i == 0 || tr.IsWhitespace() {
			continue
		}
		var tags []string
		for _, r := range tr.GetReadings() {
			if r != nil && r.GetPOSTag() != nil {
				tags = append(tags, *r.GetPOSTag())
			}
		}
		all = append(all, tags)
	}
	return all
}

func hasExactPOS(tags []string, want string) bool {
	for _, p := range tags {
		if p == want {
			return true
		}
	}
	return false
}

func hasAnyAnglePOS(tags []string) bool {
	for _, p := range tags {
		if strings.Contains(p, "<") {
			return true
		}
	}
	return false
}
